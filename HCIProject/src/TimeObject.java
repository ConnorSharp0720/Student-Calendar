
public class TimeObject
{
    private long time; 
    
    public TimeObject(long time)
    {
        this.time = time;
    }

    /**
     * @return the time
     */
    public long getTime()
    {
        return time;
    }

    /**
     * @param time the time to set
     */
    public void setTime(long time)
    {
        this.time = time;
    }
    
    
}


